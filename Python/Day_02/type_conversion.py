# This program takes the age of the user as input and adds 5 to it.
# without the int() function, the input will be treated as a string and throws error when trying to add 5 to it.
age = int(input("Enter your age: "))
print(age + 5)

# This program takes the height of the user as input and converts it to a float.
height = float(input("Enter your height in meters: "))
print(height)